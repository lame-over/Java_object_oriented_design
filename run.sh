#!/bin/bash
# Скрипт для компіляції та запуску Java-програми LMS

echo "=========================================="
echo "LMS - Система управління навчанням"
echo "=========================================="
echo ""

# Переходимо до папки з вихідним кодом
SRC_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)/src"

echo "Папка з кодом: $SRC_DIR"
echo ""

# Компіляція
echo "Компіляція Java-файлів..."
cd "$SRC_DIR"
javac *.java

if [ $? -eq 0 ]; then
    echo "✓ Компіляція успішна!"
    echo ""
    echo "Запуск програми..."
    echo ""
    java Main
else
    echo "✗ Помилка при компіляції"
    exit 1
fi
